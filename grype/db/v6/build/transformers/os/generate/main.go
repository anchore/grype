// Generates rhel_minor_span_generated.go from Red Hat's official product life-cycle
// API (https://access.redhat.com/product-life-cycles). Each RHEL major's span is its
// final minor release: the planned final for majors still in support (stable once
// published) and the historical final for retired majors, so regeneration matters
// only when Red Hat announces a new major.
package main

import (
	"encoding/json"
	"fmt"
	"io"
	"net/http"
	"os"
	"strconv"
	"strings"

	"github.com/dave/jennifer/jen"
)

const (
	apiURL     = "https://access.redhat.com/product-life-cycles/api/v1/products?name=Red%20Hat%20Enterprise%20Linux"
	outputPath = "rhel_minor_span_generated.go" // relative to the os package (where go:generate runs)
)

// retiredFinalMinors are the final minor releases of RHEL majors that the life-cycle
// API no longer serves (retired products). These are historical facts and can never
// change. When the API stops serving a major (the sanity check below fails), move that
// major's final minor here.
var retiredFinalMinors = map[string]int{
	"5": 11, // RHEL 5 ended at 5.11
	"6": 10, // RHEL 6 ended at 6.10
	"7": 9,  // RHEL 7 ended at 7.9
}

// liveMajors are the majors the API is expected to serve today. This is a lower bound
// only: new majors are picked up automatically, but a major disappearing from the API
// must be an explicit, reviewed event (moved into retiredFinalMinors), not a silent
// shrink of the generated map.
var liveMajors = []string{"8", "9", "10"}

type response struct {
	Data []struct {
		Name     string `json:"name"`
		Versions []struct {
			Name              string `json:"name"`
			FinalMinorRelease string `json:"final_minor_release"`
		} `json:"versions"`
	} `json:"data"`
}

func main() {
	spans := make(map[string]int)
	for major, minor := range retiredFinalMinors {
		spans[major] = minor
	}

	fmt.Println("Fetching RHEL life-cycle data from", apiURL)
	resp, err := http.Get(apiURL) //nolint:gosec
	if err != nil {
		panic(fmt.Errorf("error fetching data from %s: %w", apiURL, err))
	}
	defer resp.Body.Close()

	if resp.StatusCode != http.StatusOK {
		panic(fmt.Errorf("unexpected status %d from %s", resp.StatusCode, apiURL))
	}

	data, err := io.ReadAll(resp.Body)
	if err != nil {
		panic(fmt.Errorf("error reading response: %w", err))
	}

	var parsed response
	if err := json.Unmarshal(data, &parsed); err != nil {
		panic(fmt.Errorf("error parsing JSON: %w", err))
	}

	for _, product := range parsed.Data {
		for _, v := range product.Versions {
			major, minor, err := parseFinalMinor(v.Name, v.FinalMinorRelease)
			if err != nil {
				panic(fmt.Errorf("version %q: %w", v.Name, err))
			}
			if prev, ok := spans[major]; ok && prev != minor {
				panic(fmt.Errorf("conflicting final minor for major %s: %d vs %d (retired majors must not reappear in the API with a different value)", major, prev, minor))
			}
			fmt.Printf("  rhel %s -> final minor release %s.%d\n", major, major, minor)
			spans[major] = minor
		}
	}

	for _, major := range liveMajors {
		if _, ok := spans[major]; !ok {
			panic(fmt.Errorf("life-cycle API no longer serves RHEL %s; record its final minor in retiredFinalMinors and update liveMajors", major))
		}
	}

	render(spans)
	fmt.Printf("Code generation completed and written to %s\n", outputPath)
}

// parseFinalMinor validates that a version entry's name is a bare major (e.g. "9") and
// its final_minor_release is that major plus a minor (e.g. "9.10"), returning both.
func parseFinalMinor(name, finalMinorRelease string) (major string, minor int, err error) {
	if _, err := strconv.Atoi(name); err != nil {
		return "", 0, fmt.Errorf("version name is not a bare major: %w", err)
	}
	prefix, minorStr, found := strings.Cut(finalMinorRelease, ".")
	if !found {
		return "", 0, fmt.Errorf("final_minor_release %q is not in major.minor form", finalMinorRelease)
	}
	if prefix != name {
		return "", 0, fmt.Errorf("final_minor_release %q does not belong to major %q", finalMinorRelease, name)
	}
	minor, err = strconv.Atoi(minorStr)
	if err != nil || minor < 0 {
		return "", 0, fmt.Errorf("final_minor_release %q has a non-numeric minor", finalMinorRelease)
	}
	return name, minor, nil
}

func render(spans map[string]int) {
	f := jen.NewFile("os")
	f.HeaderComment("DO NOT EDIT: generated by grype/db/v6/build/transformers/os/generate/main.go")

	f.Comment(`rhelMinorSpan is the inclusive highest RHEL minor for which we materialize a per-minor
GA operating_system row, per major: each major's final minor release, per Red Hat's
product life-cycle data (the planned final for majors still in support, the historical
final for retired ones).

It MUST be uniform across every record of a given major: grype resolves a host to its
most-specific OS row and does NOT union the major-only row back in
(operating_system_store.go:searchForOSExactVersions), so a minor row that exists for
some packages but is absent for others would silently drop the missing packages ->
false negatives. Making every record expand across the same span guarantees each minor
row is complete.

Erring low is safe: a host on a minor beyond the span finds no exact row and falls back
to the major-only row (today's coarse, roll-forward-to-highest behavior). Erring high
only wastes rows (extra minors carry the highest fix, same as the major fallback).`)
	f.Var().Id("rhelMinorSpan").Op("=").Map(jen.String()).Int().Values(jen.DictFunc(func(d jen.Dict) {
		for major, minor := range spans {
			d[jen.Lit(major)] = jen.Lit(minor)
		}
	}))

	file, err := os.OpenFile(outputPath, os.O_RDWR|os.O_CREATE|os.O_TRUNC, 0644)
	if err != nil {
		panic(fmt.Errorf("unable to open file: %w", err))
	}
	defer file.Close()

	if _, err := fmt.Fprintf(file, "%#v", f); err != nil {
		panic(fmt.Errorf("unable to write file: %w", err))
	}
}
